package br.com.golsoftware.financeiro.dominio.modelo;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.SequenceGenerator;

@Entity
@SequenceGenerator(name = "grupo_despesa_sequence", sequenceName = "sq_grupo_despesa")
public class GrupoDespesa {

	@Id
	@GeneratedValue(strategy = GenerationType.SEQUENCE, generator = "grupo_despesa_sequence")
	private Long id;

	private String codigo;
	private String descricao;
	
	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getCodigo() {
		return codigo;
	}

	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}

	public String getDescricao() {
		return descricao;
	}

	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}

}
